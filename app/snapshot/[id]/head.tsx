interface Props {
  params: Promise<{ id: string }>;
}

export default async function Head({ params }: Props) {
  const { id } = await params;

  return (
    <>
      <title>Snapshot {id} – Blueprintly</title>
      <meta
        name="description"
        content="View architecture documentation snapshot generated by Blueprintly for your repository."
      />
    </>
  );
}

